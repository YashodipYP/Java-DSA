public class ArrayBasics {
    public static void main(String[] args) {
        int[] x = {7,49,31,37,90,8};

        //Indexing
        System.out.println(x[2]);

        //Updating Elements
        x[3]=89;
        System.out.println(x[3]);

        //Initializing array in more ways
        int[] arr = new int[4];
        arr[0]= 10;
        arr[1]= 100;
        arr[2]= 1000;
        arr[3]= 10000;

    }
}
